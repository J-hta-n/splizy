// Reverse mapping from shorthand to currency code (for input autocomplete)
export const SHORTHAND_TO_CODE: Record<string, string> = Object.entries(
  CURRENCY_SHORTHANDS,
).reduce(
  (acc, [code, shorthand]) => {
    acc[shorthand] = code;
    return acc;
  },
  {} as Record<string, string>,
);

/**
 * Convert user input (shorthand or code) to the proper currency code
 * E.g., "RM" -> "MYR", "MYR" -> "MYR", "USD" -> "USD"
 */
export const normalizeCurrencyInput = (input: string): string => {
  const upper = input.toUpperCase();
  return SHORTHAND_TO_CODE[upper] || upper;
};
